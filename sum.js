/**
 * Calcula la suma de dos números.
 *
 * @param {number} a - El primer número.
 * @param {number} b - El segundo número.
 * @returns {number} La suma de los dos números.
 */
function sum(a, b) {
 return a + b;
}

/**
 * Exporta la función sum para su uso en otros módulos.
 */
module.exports = sum;